def get_character(slug):
    return next((character for character in characters if character["slug"] == slug), None)


def get_all_characters():
    return characters


def get_characters_by_affiliation(affiliation):
    return [c for c in characters if any(affiliation.lower() in a.lower() for a in c.get("affiliations", []))]


